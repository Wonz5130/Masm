DATA SEGMENT USE16
     BUF DB '3','4','8','9'
     COUNT EQU $-BUF
DATA ENDS

CODE SEGMENT USE16
    ASSUME CS:CODE,DS:DATA

BEG:
	MOV AX,DATA
	MOV DS,AX
	MOV CX,COUNT ;循环4次
	MOV AX,0  ;AX存放累加的和
	MOV SI,OFFSET BUF
	MOV DH,0  ;DH清零
	MOV BX,10
AGA:
	MUL BX
	MOV DL,[SI]
	SUB DL,30H  ;用减法
	ADD AX,DX
	INC SI
	LOOP AGA
	MOV BX,AX
	CALL DISP
	MOV AH,02H
	MOV DL,'B'
	INT 21H
EXIT:
	MOV AH,4CH
	INT 21H

DISP PROC
	MOV CX,16
LAST:
	MOV DL,'0'
	RCL BX,1
	;CMP BX,30H
	JNC NEXT
	MOV DL,'1'
NEXT:
	MOV AH,02H
	INT 21H
	LOOP LAST
	RET
DISP ENDP
CODE ENDS
	END BEG
